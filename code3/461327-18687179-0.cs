        public static int CountOn(int value)
        {
            return _precomputed[value >> 24] +
                   _precomputed[(value << 8) >> 24] +
                   _precomputed[(value << 16) >> 24] +
                   _precomputed[value & 0xFF];
        }
        public static int CountOff(int value)
        {
            return 32 - CountOn(value);
        }
